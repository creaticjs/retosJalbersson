function actualizarDatos()
{
    var index = selEquipos.selectedIndex;
    nomEquipo.innerHTML = dataEquipos[index].nombre;
    imagenEquipo.setAttribute("src", dataEquipos[index].urlImage);
    labelNombre.innerText = "Nombre: " + dataEquipos[index].nombre;
    labelDT.innerText = "Director Técnico: " + dataEquipos[index].dt;
    for(var i=0; i<dataEquipos[index].jugadores.length; i++)
    {
        listadoJugadores += dataEquipos[index].jugadores[i] + " ";
    }
    labelJugadores.innerText = "Jugadores: " + listadoJugadores;
    labelResenia.innerText = "Reseña: " + dataEquipos[index].resenia;
    limpiarDiv();
    crearEstrellas(index, dataEquipos[index].estrellas);
}

function crearEstrellas(index, estrellas)
{
    for(var i=0; i<estrellas; i++)
    {
        var estrella = document.createElement("IMG");
        if(dataEquipos[index].nombre == "Nacional")
        {
            estrella.src = "res/croisant.png";
        }
        else
        {
            estrella.src = "res/estrella.png";
        }
        estrella.style = "height: 20px; width: 20px";
        divEstrellas.appendChild(estrella);
    }
}

function limpiarDiv()
{
    while (divEstrellas.firstChild) 
    {
        divEstrellas.removeChild(divEstrellas.firstChild);
    }
    var label = document.createElement("LABEL");
    label.innerText = "Estrellas: ";
    label.style = "font-weight: bold";
    divEstrellas.appendChild(label);
}

function mostrarTablaAsc()
{
    var aux = "";
    var vectorOrdenado = dataEquipos.sort
    (
        function(objetoA, objetoB)
        {
            if(parseInt(objetoA.puntos) > parseInt(objetoB.puntos))
            {
                return -1;
            } else if(parseInt(objetoA.puntos) < parseInt(objetoB.puntos)){
                return 1;
            }
            else
            {
                return 0;
            }
        }
    );
    vectorOrdenado.forEach(function (equipo) 
    {
        aux += "<tr><th scope='row'></th><td>"+equipo.nombre+"</td><td>"+equipo.partidosJugados+"</td><td>"+equipo.ganados+"</td><td>"+equipo.empatados+"</td><td>"+equipo.perdidos+"</td><td>"+equipo.golesAFavor+"</td><td>"+equipo.golesEnContra+"</td><td>"+equipo.puntos+"</td>";
    });
    cuerpoT.innerHTML = aux;
}

